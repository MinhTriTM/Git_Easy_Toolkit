def browse_folder_gui():
    if not HAS_TKINTER:
        return None
    root = tk.Tk()
    root.withdraw() # Ẩn cửa sổ chính
    root.attributes('-topmost', True) # Hiện lên trên cùng
    folder_path = filedialog.askdirectory(title="Chọn thư mục làm việc Git")
    root.destroy()
    return folder_path

def ask_directory():
    config = load_config()
    history = config.get("history", [])
    
    while True:
        clear_screen()
        print(f"{C_CYAN}{C_BOLD}=== CHỌN THƯ MỤC LÀM VIỆC ==={C_RESET}")
        
        print(f"\n{C_YELLOW}💡 Gợi ý: Kéo thả thư mục vào màn hình đen này, hoặc chọn từ Menu dưới đây:{C_RESET}")
        if HAS_TKINTER:
            print(f"\n  {C_BOLD}[0] 📂 Mở cửa sổ chọn thư mục (Duyệt bằng chuột){C_RESET}")
            
        if history:
            print(f"\n{C_YELLOW}🕒 Lịch sử gần đây:{C_RESET}")
            for idx, path in enumerate(history):
                print(f"  [{idx + 1}] {path}")
            print(f"\n>> Gõ {C_GREEN}[Số]{C_RESET} để chọn, hoặc {C_GREEN}Kéo Thả{C_RESET} thư mục mới vào đây:")
        else:
            print(f"\n>> Nhập đường dẫn thư mục:")
            
        raw_path = input(f">> {C_RESET}").strip()
        
        # Nếu chọn Mở Cửa Sổ GUI
        if raw_path == '0' and HAS_TKINTER:
            print(f"{C_DIM}Đang mở cửa sổ chọn thư mục...{C_RESET}")
            selected_dir = browse_folder_gui()
            if selected_dir and os.path.isdir(selected_dir):
                save_config(selected_dir)
                return selected_dir
            else:
                print(f"{C_RED}❌ Đã hủy chọn thư mục.{C_RESET}")
                input("Nhấn Enter để thử lại...")
                continue
                
        # Nếu nhập số từ lịch sử
        if raw_path.isdigit() and 1 <= int(raw_path) <= len(history):
            target = history[int(raw_path) - 1]
            if os.path.isdir(target):
                save_config(target)
                return target
            else:
                print(f"{C_RED}❌ Thư mục này không còn tồn tại trên máy!{C_RESET}")
                history.remove(target) # Xóa khỏi lịch sử ảo
                # Lưu lại config để loại bỏ thư mục hỏng
                config["history"] = history
                with open(CONFIG_FILE, 'w', encoding='utf-8') as f:
                    json.dump(config, f, indent=4)
                input("Nhấn Enter...")
                continue
                
        target_dir = raw_path.strip('"').strip("'")
        target_dir = os.path.expanduser(target_dir)

        if os.path.isdir(target_dir):
            save_config(target_dir)
            return target_dir
        else:
            print(f"{C_RED}❌ Thư mục không tồn tại. Vui lòng thử lại!{C_RESET}")
            input("Nhấn Enter để tiếp tục...")



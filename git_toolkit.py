def clear_screen():
    os.system('cls' if os.name == 'nt' else 'clear')

def check_git_installed():
    try:
        subprocess.run(["git", "--version"], stdout=subprocess.PIPE, stderr=subprocess.PIPE, check=True)
        return True
    except FileNotFoundError:
        return False

def run_git_cmd(cmd_list, cwd, capture_output=False, show_progress=False, check=True):
    """
    show_progress=True sẽ để nguyên stdout/stderr để thấy được thanh tải của Git
    """
    try:
        if capture_output:
            result = subprocess.run(cmd_list, cwd=cwd, stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True, check=check)
            return True, result.stdout.strip()
        else:
            print(f"{C_DIM}>> Đang chạy: {' '.join(cmd_list)}{C_RESET}")
            # Nếu cần show progress, không dùng PIPE
            stderr_target = None if show_progress else subprocess.PIPE
            stdout_target = None if show_progress else subprocess.PIPE
            
            subprocess.run(cmd_list, cwd=cwd, stdout=stdout_target, stderr=stderr_target, check=check)
            return True, ""
    except subprocess.CalledProcessError as e:
        err_msg = e.stderr if e.stderr else str(e)
        return False, err_msg

def get_git_info(cwd):
    """Lấy thông tin Branch và Status để vẽ Dashboard"""
    if not os.path.isdir(os.path.join(cwd, ".git")):
        return "Chưa khởi tạo Git", "Không có dữ liệu", "Chưa cấu hình", "Chưa cấu hình"
    
    branch = "Unknown"
    status_summary = "Sạch (Clean)"
    user_name = "Chưa cấu hình"
    user_email = "Chưa cấu hình"
    
    # Lấy branch
    ok, out = run_git_cmd(["git", "branch", "--show-current"], cwd, capture_output=True, check=False)
    if ok and out:
        branch = out
        
    # Lấy status
    ok, out = run_git_cmd(["git", "status", "--porcelain"], cwd, capture_output=True, check=False)
    if ok:
        if out:
            lines = out.split('\n')
            status_summary = f"{len(lines)} file(s) thay đổi"
            
    # Lấy Account
    ok, out = run_git_cmd(["git", "config", "user.name"], cwd, capture_output=True, check=False)
    if ok and out: user_name = out
    ok, out = run_git_cmd(["git", "config", "user.email"], cwd, capture_output=True, check=False)
    if ok and out: user_email = out
            
    return branch, status_summary, user_name, user_email

def auto_generate_gitignore(cwd):
    """Tự động quét và tạo .gitignore nếu chưa có"""
    gitignore_path = os.path.join(cwd, ".gitignore")
    if os.path.exists(gitignore_path):
        return
        
    ignores = []
    if os.path.exists(os.path.join(cwd, "package.json")):
        ignores.extend(["node_modules/", "npm-debug.log", ".env"])
    has_python = any(f.endswith('.py') for f in os.listdir(cwd) if os.path.isfile(os.path.join(cwd, f)))
    if has_python:
        ignores.extend(["__pycache__/", "*.pyc", ".venv/", "venv/", ".env"])
        
    ignores.extend([".DS_Store", "Thumbs.db"])
    
    if ignores:
        with open(gitignore_path, 'w', encoding='utf-8') as f:
            f.write("# Auto-generated by Git Easy Toolkit\n")
            f.write("\n".join(ignores) + "\n")
        print(f"{C_GREEN}✨ Tự động tạo file .gitignore cho dự án!{C_RESET}")


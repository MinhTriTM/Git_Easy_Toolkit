def menu_daily_flow(target_dir):
    while True:
        clear_screen()
        print(f"{C_CYAN}{C_BOLD}=== 2. QUY TRÌNH HÀNG NGÀY (DAILY FLOW) ==={C_RESET}")
        print(" 1. Pull   : Cập nhật code mới nhất từ team")
        print(" 2. Status : Xem mình đã sửa những file nào")
        print(" 3. Push   : Đẩy code (Bao gồm Add + Commit + Push tự động)")
        print(f" {C_RED}0. Quay lại{C_RESET}")
        
        choice = input(f">> Chọn: ").strip()
        if choice == '1':
            branch = get_git_info(target_dir)[0]
            ok, err = run_git_cmd(["git", "pull", "origin", branch], target_dir, show_progress=True)
            if not ok and ("conflict" in str(err).lower() or "merge" in str(err).lower()):
                print(f"\n{C_RED}🔥 XUNG ĐỘT (CONFLICT) 🔥{C_RESET}")
                ans = input(">> Xóa thay đổi của bạn lấy trên mạng? (y/N): ").strip().lower()
                if ans == 'y':
                    run_git_cmd(["git", "reset", "--hard", f"origin/{branch}"], target_dir)
                else:
                    run_git_cmd(["git", "merge", "--abort"], target_dir)
            pause()
        elif choice == '2':
            run_git_cmd(["git", "status"], target_dir, show_progress=True)
            pause()
        elif choice == '3':
            auto_generate_gitignore(target_dir)
            status = get_git_info(target_dir)[1]
            if status == "Sạch (Clean)":
                print(f"{C_YELLOW}⚠️ Không có thay đổi nào mới để Push!{C_RESET}")
            else:
                msg = input(">> Nhập lời nhắn (Commit Message): ").strip()
                if not msg: msg = "Cập nhật code tự động"
                branch = get_git_info(target_dir)[0]
                run_git_cmd(["git", "add", "."], target_dir)
                run_git_cmd(["git", "commit", "-m", msg], target_dir)
                run_git_cmd(["git", "push", "--progress", "origin", branch], target_dir, show_progress=True)
            pause()
        elif choice == '0':
            break


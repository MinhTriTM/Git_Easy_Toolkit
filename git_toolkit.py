CONFIG_FILE = os.path.join(os.path.dirname(os.path.abspath(__file__)), "config.json")

def load_config():
    if os.path.exists(CONFIG_FILE):
        try:
            with open(CONFIG_FILE, 'r', encoding='utf-8') as f:
                return json.load(f)
        except:
            return {"history": []}
    return {"history": []}

def save_config(target_dir):
    config = load_config()
    history = config.get("history", [])
    
    if target_dir in history:
        history.remove(target_dir)
    history.insert(0, target_dir) # Đưa lên đầu
    
    # Chỉ giữ 5 lịch sử gần nhất
    config["history"] = history[:5]
    
    with open(CONFIG_FILE, 'w', encoding='utf-8') as f:
        json.dump(config, f, indent=4)

